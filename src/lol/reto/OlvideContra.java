    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
       
    try {

        
            String dni,record;
            int conta=0;
            dni=txt_dni.getText();

            BufferedReader br=new BufferedReader(new FileReader("Crear cuenta.txt"));

            while((record=br.readLine())!=null){

                StringTokenizer st =new StringTokenizer(record,",");
                if(record.contains(dni)){

                    conta ++;}
                if(conta>0){
                    JOptionPane.showMessageDialog(null,"desea ver sus datos");

                    txt_resultado.setText("Nombres: "+st.nextToken()+"\nEdad: "+st.nextToken()+"\nDNI: "+st.nextToken()+"\nUsuario:"+st.nextToken()+"\nContrase√±a:"+st.nextToken());

                }else{
                    JOptionPane.showMessageDialog(null,"Datos no encontrados");
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(registrarse.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(registrarse.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2MouseClicked

