    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
try {
            String user,pass,record;
            int conta = 0;
            user = txt_user.getText();
            pass = txt_clave.getText();

            BufferedReader br = new BufferedReader ( new FileReader("Crear cuenta.txt"));
            while(( record = br.readLine()  )       != null) {
            StringTokenizer st = new StringTokenizer(record ,",");
            if ( record.contains(user) &&  record.contains(pass) )  {
              System.out.println("|"+st.nextToken()+"  "+st.nextToken()+"   "+st.nextToken()+" "
                      + "  "+st.nextToken()+"   "+st.nextToken()+"|");
            
                   conta ++;
               }
            }
            if (conta>0) {
            JOptionPane.showMessageDialog(null, "Ingresaste al Sistema");
            
                bienvenido b = new  bienvenido();
                b.setVisible(true);
                this.setVisible(false);
                
            }else{
           JOptionPane.showMessageDialog(null, "Usuario o Password no existe");
            }
                    
        } catch (FileNotFoundException ex) {
            System.out.println(ex.getMessage());
        } catch (IOException e) {
            System.out.println(e.getMessage());
        }
     
       
    }//GEN-LAST:event_jButton3MouseClicked

